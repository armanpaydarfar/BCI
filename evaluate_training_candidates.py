#!/usr/bin/env python3
"""
Evaluate candidate training sessions for the PILOT007 XGBoost motor imagery
decoder using covariance-space discriminability + per-session LOO contribution.

Methodology is documented in SoftwareDocs/projects/harmony-bci/datasets/selection-report.md
(generated by this script).

Stages (independently runnable via --stage):
  cache     -- load XDFs, segment, whiten covariances (mu + beta), save pickle
  fisher    -- Riemannian Fisher score per session in mu and beta bands
  direction -- per-session MI/REST discriminant direction + pairwise alignment
               (diagnoses "anti-aligned" sessions whose MI direction is the
               pool's REST direction — this is what Fisher magnitude misses)
  loo       -- session-LOO baseline + per-session ablation contributions using
               concatenated mu+beta tangent features, matching production XGB
  report    -- write the Markdown report from cache contents
  all       -- cache + fisher + direction + loo + report

Reuses the canonical xgb_cov pipeline without duplicating preprocessing:
  - Utils.stream_utils.load_xdf
  - Utils.xgb_feature_pipeline.segment_and_extract_cov_erd
  - Generate_Riemannian_adaptive.compute_processed_covariances(model_type='xgb')
"""

