# ---------------- shared helpers ----------------

def _parse_subject_session(fname: str) -> tuple[str, str]:
    m = FILENAME_RE.search(fname)
    if not m:
        raise ValueError(f"cannot parse subject/session from filename: {fname}")
    return m.group(1), m.group(2)


def _collect_candidates() -> list[dict]:
    """Walk the four category directories, dedupe by filename, preserve category aliases."""
    seen: dict[str, dict] = {}
    for category, rel in CATEGORY_DIRS:
        full = os.path.join(TRAINING_DATA_ROOT, rel)
        if not os.path.isdir(full):
            print(f"  [warn] missing category dir: {full}", file=sys.stderr)
            continue
        for fname in sorted(os.listdir(full)):
            if not fname.endswith(".xdf") or "OBS" in fname:
                continue
            fpath = os.path.join(full, fname)
            if fname in seen:
                seen[fname]["aliased_categories"].append(category)
                continue
            subj, sess = _parse_subject_session(fname)
            seen[fname] = {
                "file_path": fpath,
                "subject_id": subj,
                "session_tag": sess,
                "visual_category": category,
                "aliased_categories": [],
            }
    return sorted(seen.values(), key=lambda s: (s["subject_id"], s["session_tag"]))


def _fit_tangent_ref(covs: np.ndarray) -> np.ndarray:
    ts = TangentSpace(metric="riemann")
    ts.fit(covs)
    return ts.reference_


def _project(covs: np.ndarray, ref: np.ndarray) -> np.ndarray:
    return tangent_space(covs, ref, metric="riemann")


def _get_xgb_params() -> dict:
    """Mirror train_xgb_dual_thresholds parameter construction, without plotting."""
    p = dict(
        n_estimators=int(getattr(config, "XGB_N_ESTIMATORS", 300)),
        max_depth=int(getattr(config, "XGB_MAX_DEPTH", 6)),
        objective="binary:logistic",
        eval_metric="logloss",
        random_state=42,
        verbosity=0,
    )
    for pname, ckey, cast in [
        ("learning_rate",    "XGB_LEARNING_RATE",    float),
        ("subsample",        "XGB_SUBSAMPLE",        float),
        ("colsample_bytree", "XGB_COLSAMPLE_BYTREE", float),
        ("reg_alpha",        "XGB_REG_ALPHA",        float),
        ("reg_lambda",       "XGB_REG_LAMBDA",       float),
        ("min_child_weight", "XGB_MIN_CHILD_WEIGHT", float),
    ]:
        v = getattr(config, ckey, None)
        if v is not None:
            p[pname] = cast(v)
    return p


def _load_cache(cache_path: str) -> dict:
    with open(cache_path, "rb") as f:
        return pickle.load(f)


def _save_cache(cache_path: str, cache: dict) -> None:
    os.makedirs(os.path.dirname(cache_path), exist_ok=True)
    with open(cache_path, "wb") as f:
        pickle.dump(cache, f)


